public class Arithmetic {
public static void main(String[] args) {
    int a = 8;
    int b = 5;
    
    System.out.println(a + b);
    System.out.println(a - b);
    System.out.println(a * b);
    System.out.println(a / b);
    System.out.println(a % b);
    System.out.println("\n");
    
    double c = 5;
    double d = 3;
    
    System.out.println(c + d);
    System.out.println(c - d);
    System.out.println(c * d);
    System.out.println(c / d);
}
}
